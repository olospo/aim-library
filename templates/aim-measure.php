        <section class="measure-section">
          <h2>Overview</h2>
          <p>The Adolescent Cognitive Style Questionnaire (Hankin &amp; Abramson, 2002) was adapted from an adult measure for cognitive style described by Metalsky &amp; Joiner, 1992.</p>
    
          <p>The measure is made up of hypothetical situations, adapted to be relevant to adolescents' experiences. Respondents are asked to write possible causes for hypothetical situations and rate the likelihood of further negative consequences arising, as well as the extent to which the event would indicate they are flawed.</p>
        </section>
    
        <section class="measure-section">
          <h2>Scoring &amp; Interpretation</h2>
          <p>Each of 12 hypothetical scenarios has 5 questions, assessing different domains of cognitive style. For each of the five domains, the mean score is taken for responses, for example the mean score for part 'a' responses for each of the five scenarios.</p>
    
          <p>The five domains assessed are: internality in cognitive style; global cognitive style; stable cognitive style; cognitive style relating to consequences; and cognitive style relating to self.</p>
        </section>
    
        <section class="measure-section">
          <h2>Psychometric Properties</h2>
    
          <div class="properties-list">
            <div class="property-item">
              <h3>Internal Consistency</h3>
              <p>
                Hankin &amp; Abramson, 2002 identified internal consistency of the overall ASCQ to be a = .96. The attributional styles subscale had a = .91. This subscale was also split into a generality subscale (a = .91) and internality (a = .81). For negative inferences about consequence, a = .91, and for negative inferences about self, a = .93.
              </p>
            </div>
    
            <div class="property-item">
              <h3>Test-retest Reliability</h3>
              <p>
                Test-retest reliability over 2 weeks = .73. Over two years, stability was .51 (Hankin and Abramson, 2002).
              </p>
            </div>
    
            <div class="property-item">
              <h3>Construct Validity</h3>
              <p>
                Construct Validity not defined.
              </p>
            </div>
    
            <div class="property-item">
              <h3>Structural Validity</h3>
              <p>
                3-factor structure supported (Hankin and Abramson, 2002): negative inferences for cause (stable and global attributions), negative inferences for consequence, and negative inferences about the self. This fit the data — Chi squared = 36.05, df = 6, p &lt; .01. TLI = .97, CFI = .99. A one-factor model was also tested, but this did not fit so well.
              </p>
            </div>
    
            <div class="property-item">
              <h3>Convergent Validity</h3>
              <p>
                In terms of construct validity, Hankin and Abramson showed that overall ACSQ score and the subscale scores interacted significantly with negative events to predict general depressive symptoms and raw scores for anxiety/depression.
              </p>
            </div>
    
            <div class="property-item">
              <h3>Divergent Validity</h3>
              <p>
                The ACSQ was not associated with externalising behaviours (Hankin &amp; Abramson, 2002).
              </p>
            </div>
    
            <div class="property-item">
              <h3>Measurement Invariance</h3>
              <p>
                Not reported.
              </p>
            </div>
